var a = 50;
var zerozeun = 0;
var onezeun = 0;

for (var i = 1; i <= a; ++i) {
  if(i%2 == 0) {
    zerozeun = zerozeun + i
    console.log("짝수" , i)
  } else {
    onezeun = onezeun + i
    console.log("홀수" , i)
  };
};

console.log("짝수의 합 :", zerozeun,"홀수의 합 :", onezeun);

